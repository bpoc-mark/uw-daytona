                <li class="list--item">
                  <h3 class="ttl">機能とデザインの両立が、<br>ライダーから支持される<br class="sp">カスタマイズに。</h3>
                  <p class="desc">
                    1974年から国内向けにブランド名「デイトナ」の使用を開始。創業時に目指した自由にカスタムを楽しむ象徴を「アメリカ時代に見たデイトナの光景」と重ね、ブランドイメージもアメリカを意識したものを前面に押し出していった。<br>
                    1975年、日本初となるアルミ合金製「セブンスターキャストホイール」を発売。メーカー純正はスポークホイールが当たり前の時代で、海外レースで使われ始めたキャストホイールは時代の最先端。日本のライダーは羨望の眼差しで見ていた。阿部は「誰もやっていないからこそ、うちが最初に挑戦したい。どうにかして国産のアルミキャストホイールを作れないか」と製造メーカーを説得し、「カスタムパーツは見た目のデザインも大事」と７本スポークのキャストホイールを完成させて販売を開始。その多くは本家アメリカに輸出され、７本スポークのバランスの良さとデザイン性の高さが評判を呼び、「ヘンリーアベ」の名は本場ライダーの脳裏に刻まれることとなった。<br>
                    1976年には本社を静岡県磐田市に移転。ホンダ・スズキ・ヤマハという国内３メーカーが発祥したバイクのふるさと・遠州地域は二輪産業の一大集積地。メーカーにパーツを出荷する多くの企業が集まっていた。この移転を契機に、これまで以上にオリジナルパーツ開発が加速していくことになる。<br>
                    1984年、リプレイスブレーキパッド「赤パッド」を発売開始。パッドが減ってくれば純正品と交換するのが一般的な時代に、リーズナブルで高い制動力を発揮する赤パッドの発売は画期的であった。特に赤く塗装したベースプレートが強い印象を与え、これも「カスタムパーツは見た目が大事」という阿部の信念から生み出されたものだった。ライダーたちはキャリパーの奥にチラリと見える赤に「交換しただけで速く走れそう」なイメージを持ち、パッドの交換がバイクカスタムになるという新たな領域を作り出した。発売から30年以上経過した今でも高い支持を受ける、超ロングセラー商品はこうして誕生した。
                  </p>
