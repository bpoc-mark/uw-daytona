                  <h3 class="ttl">常に社会を見続ける、<br class="sp">先見の明が産んだ大型用品店。</h3>
                  <p class="desc">
                    「バイク文化創造企業」を標榜するデイトナ。日本のバイク文化を語るとき、社会から正しく認知され市民権を得なければ文化とは言えない。<br>
                    1993年に国内最大規模のバイク用品店ライコランド千葉を1号店としてオープン。これは横浜にできたトイざらスを視察した阿部が、その多様な品揃えと家族が楽しんで買い物をする姿を見て「これからはバイク業界も同じように、ライダーがさまざまな用品に触れて買い物を楽しむ時代がくる」という先見の明で発案。他社に先駆けて今に続く大型用品店を開店させた。「デイトナ社員には、いろんなものを興味を持って『見る』意識を持って欲しい。見ていれば何かのときに、ヒントになることが必ずある。見なかったらおそらく何も生まれない」。<br>
                    1997年、デイトナがプロデュースして、英国BSAリーガル社が製造した「BSA SR」を世界限定200台で発売。外装を変えた３タイプが製造され、フェザーベッドフレームにSRの単気筒エンジンを積んだ姿は、まさに正統派カフェレーサーそのもの。熱狂的なエンスージアストから高い評価を得た。<br>
                    2001年、静岡県森町に物流拠点と本社機能を移転・集約した新社屋が完成。外周550mのロードコースと160mのダートコースを併せ持つ本格的なテストコースを備え、開発において自社内でスピーディーにテストできる環境を整備、他社を圧倒する多様なジャンルの商品展開が加速していった。<br>
                    「これからもデイトナはチャレンジ精神を忘れずに、良い会社に変えていって欲しいと思う。良い会社とは成長を続ける会社。成長を続けるにはバイクに限らず、お客様に喜んでもらえる分野にどんどん挑戦して欲しい」と阿部は語る。<br>
                    常に挑戦をしてきた阿部のビジネス哲学は、今もデイトナの社員一人ひとりに脈々と受け継がれている。
                  </p>
