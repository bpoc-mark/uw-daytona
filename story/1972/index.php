                  <h3 class="ttl">フェアな土壌で<br class="sp">正々堂々と戦いたい。</h3>
                  <p class="desc">
                    阿部がビジネスで大切にしていたのはフェアという言葉。重要な判断を迫られたとき「フェアかフェアじゃないか」といつも考えたという。デイトナの歴史において、時には難しい立場におかれたこともあった。そんなときに「自分だけが得をするのではダメだ。商品を買ってくれるライダーやパーツの製造をしてくれる協力会社、何より社会のためになる商品を作らなければいけない」と常日頃から考えていた。会社が難しい局面に立った時も、それを実践することで、徐々に物事も前に進み始めたのである。<br>
                    日本で自由にバイクカスタムを楽しめるようにするには、バイクを取り巻く健全な環境づくりが必要であった。今でこそバイクのカスタムは多くの支持を集めているが、以前はバイクをカスタムする＝不正改造とネガティブに捉えられた時代であった。<br>
                    1989年、デイトナは用品店や他のメーカーとともに、JMCA（全国二輪車用品連合会）という団体を設立。法律に適合したパーツの推奨、不良品や不正改造車の一掃など、バイクが社会に与える悪影響を排除していった。社会全体がバイクを肯定的に受け入れ、そこで正々堂々とビジネスをしたいとの思いだった。今やJMCAマークのマフラーは、法適合の証として広く認知されている。さらに現在はライダー自身の安全を守るための胸部プロテクターなどの普及に力をいれている。
