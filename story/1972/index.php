              <h2>「バイク創造企業」として<br>挑戦し続けた激動の時代。</h2>
              <span class="year">1990-2004</span>
              <ul class="list">
                <li class="list--item">
                  <h3 class="ttl">レースは真剣勝負、<br class="sp">徹底的にとことんやる。</h3>
                  <p class="desc">
                    日本は70年代前半、CB750FOURや750RS（Z2）といった高性能4気筒マシンに代表される第一次バイクブームを経て、80年代には第二次バイクブームを迎えた。新車のバイクが飛ぶように売れ、レーサーレプリカが登場してバイクの性能が飛躍的に進化していった。熱狂的とも言えるバイクブームの到来は、メーカーの販売競争を加熱させ、その熱はサーキットにも波及、レースは製品の優秀性をアピールするメーカー同士の真剣勝負の舞台となっていった。<br>
                    アメリカでレースと間近に接していた阿部も、いつかレースに参戦したいと考えていた。機が熟したのは1986年。オーストラリアからスティーブ・ワッツをライダーとして招聘。チーム・デイトナとして全日本に参戦を開始した。プライベーターとしての参戦だが、レースという同じ舞台でワークスチームには絶対に負けたくないというチャレンジ精神での挑戦であった。当時チーム・デイトナはミニバイクを底辺に国内B級、国内A級、そして国際A級（250cc）を頂点とするデイトナレーシングシステムというピラミッド型のサポート体制を確立。のちに全日本や世界で活躍する芳賀健輔・紀行兄弟や中須賀（克行）選手が若手有望株として所属し、レースの腕を磨いていった。最新鋭のマシンと整備を担当するメカニックサポートを備え、プライベーターとしては破格の規模での参戦だった。<br>
                    「レースは徹底的にやらなければダメ。サーキットでの競走は横一線の真剣勝負。参戦するだけでは意味がない。だからとことんやる」。レースにかける阿部の思いには鬼気迫るものがあった。だからこそチーム全員が必死になって戦った。
                  </p>
