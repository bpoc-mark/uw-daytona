                <li class="list--item">
                  <h3 class="ttl">本物の「バイク文化」を<br>創造するために始まった挑戦。</h3>
                  <p class="desc">
                    1972年、大阪に「阿部商事」という小さな貿易会社が誕生した。扱うのは主にバイクパーツの輸出入事業。後の株式会社デイトナの前身である。<br>
                    創業者のヘンリー・アベ／阿部久夫は、幼い頃からバイクが好きで「いつかはバイクの仕事をしてみたい」と思っていた。学校を卒業後、ヤマハ（当時の日本楽器二輪事業部、現ヤマハ発動機）に入社、最初はエンジン設計を担当した。その後、若手の中から抜擢されてUSヤマハに派遣。営業兼メカニックとして全米のあちこちを周り、アメリカのバイク事情を肌で感じた。阿部がそこで見たのは、マシンを自分の手でチューンして休みにローカルレースに参加する同僚や、街中を何万台というバイクが埋め尽くすデイトナバイクウィークの熱狂など。中でもデイトナビーチで出会ったGL1000にタンデムしてやってきた老夫婦の姿に感銘を受けた。「日本は“バイクづくり”という産業は大変盛んになってきた。しかしバイクの楽しみ方はどうだろう。カスタム＝改造車と白い目で見られてしまう。ここではライダーが思い思いのスタイルでバイクを自由に楽しんでいる。子どもから大人まで、バイクに乗らない人も含めて、温かな目でバイクを見守っている。なぜこんなにも違うのだろう」と大きなカルチャーショックを受けた。アメリカではバイクが社会に受け入れられ、まさに「文化」として根付いていた。<br>
                    アメリカから帰国後「もっと自由にバイクカスタムを楽しめる環境を日本にも作りたい」という思いを原動力に立ち上げたのが阿部商事であった。ここからデイトナの「バイク文化を創造する挑戦」がスタートした。
                  </p>
