                  <h3 class="ttl">打倒ワークス、<br class="sp">その想いは本物であった。</h3>
                  <p class="desc">
                    約20年間、チーム・デイトナのエースライダーとしてレースに参戦した宮崎敦選手は当時をこう振り返る。<br>
                    「阿部さんの『打倒ワークス』という想いは本物だった。TZ250（ヤマハ市販レーサー）を限界までチューンしてもワークスにはあと一歩及ばない。誰もが限界と思うような状況でも、阿部さんは決して諦めなかった。『ワークスより前で走れたら、これほど楽しいことはないよね』と言って常にプラス志向で前向きだった。その後アプリリアと直接交渉してマシンを用意してくれた時、驚くと同時にその期待に応えなければと思いました」。<br>
                    1995年全日本ロードレース選手権、菅生大会で表彰台の頂点に立ち、その思いは結実した。プライベーターがワークスチームを抑えて勝利を手にした瞬間であった。「挑戦しない後悔よりも、挑戦をして後悔したほうがいい。結果が良くても悪くても挑戦していればきっと楽しいから。たとえ結果が良かったとしても、挑戦がなかったら本当に良いとは言えない」。創業以来、常に阿部が念頭に置いていた考えが、最高の結果として実現したのである。<br>
                    2002年には世界GPのシーズン初戦、鈴鹿大会・GP250にスポット参戦した宮崎選手が独走で優勝。阿部は宮崎選手とともに表彰台へ。チーム・デイトナが世界の頂点を勝ち取った瞬間だった。<br>
                    「レースは熱中するという熱意がなければダメ」と阿部は語る。レースにはライダー、マシン、タイヤ、メカニック、チームのすべてをベストな布陣で臨む必要がある。さらに勝利にはレースにかける熱意がチームの士気を高め、マシンをゴールへと走らせる。何かを成し遂げるためには強い信念が必要ということを改めて伝えてくれる。
                  </p>
