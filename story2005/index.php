                  <h3 class="ttl">やりたいと思うことを、<br class="sp">社員自らが実現できる会社。</h3>
                  <p class="desc">
                    2005年、創業者の阿部からバトンを託され、鈴木紳一郎が2代目社長に就任する。44歳という若さでの抜擢だった。当時を振り返り鈴木は語る。「創業者と同じことをやっても駄目だし、大きく変えても駄目という難しい状況だった。これまでは阿部さんに引っ張ってきてもらったが、自らが引っ張るという立場になった。ただ強烈なリーダーシップを発揮するというより、デイトナの在り方や社会との関係性を重視して、みんなの意見に耳を傾けて良い意見をチョイスしていった」。そのベースになったのは「変わらないこと」。「デイトナは創業者の想いが込められた会社。うちには阿部さんの経営哲学をまとめた『社長趣意書』がある。そこに書かれている言葉は、総て腑に落ちることばかり。だからガラリと変えるという選択肢はなかった」。社内のスタッフには「最近どう？」と言って声をかけ、その人が何をやりたいのか聞き、良いアイデアだと思えば「それいいね。やってみれば」と焚きつけた。こうしてやる気のある社員にチャンスを与え、積極的な商品開発に繋げていった。<br>
                    2006年、「HenlyBegins」を立ち上げて、ライダー向けウェアのリリースを開始。2012年、イギリス生まれの電動アシスト自転車「A2B HYBRID24」を発売開始。「GIVI」「NOLAN」といったヨーロッパ一流ブランドの日本総代理店となり、多様化するライダーが求める商品を海外から導入。社員のやりたいという意見をすくい上げて戦略的な経営と結びつけ、カスタムパーツ開発から、今に続く総合バイクブランドへとデイトナを成長させていった。
                  </p>
