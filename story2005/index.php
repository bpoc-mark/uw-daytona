                  <h3 class="ttl">リーマンショックでも、<br class="sp">挑戦の歩みを止めなかった。</h3>
                  <p class="desc">
                    2008年に全世界を襲ったリーマンショック。デイトナも例外でなく、商品の売れ行きが伸びず、経営的に苦しい時代が数年続いた。この苦難の時代にもデイトナは挑戦の歩みを止めることはなかった。<br>
                    デイトナを特徴づける一つに「アメーバ型」の組織がある。生産工場を持たないファブレス企業だから、企画力やアイデアで何でも形にする。リーマンショックで大きく売り上げが落ち込んだ時、鈴木が取り組んだのはライダーに向けた新しい情報発信イベントと新規事業部の立ち上げ。「時代が求めるものは何か」と全員で考え、新規分野へと参入した。<br>
                    2009年11月、第1回「静岡・森町 茶ミーティング」を開催。本社がある静岡県森町への社会貢献とライダー同士が楽しく語り合う場の創出を目的に、本社施設を会場として一般ライダーに開放。業界他社に声掛けして多数のブース出展、テストコースでのメーカー合同新車試乗会、二輪専門誌の協力によるゲストトークショーなど多角的なイベントを開催。社員が自らプログラムを企画、会場整理・受付などの運営に携わり、来場したライダーをおもてなしする参加型イベントは、多くのライダーから支持を集め、年を追うごとに参加者が増える人気イベントとなっていった。これも「今の時代にライダーが求めていることは何か？」と時代のニーズに目を配り、社員全員と意識共有した結果として生まれた産物であった。<br>
                    2012年に新規事業として太陽光発電事業を開始。これからの脱酸素社会に向けた課題解決にデイトナができることは何かとの考えから事業に着手。デイトナとグループ企業で使用する電力を100%再生可能エネルギーで賄うことを掲げ、SDGs（持続可能な開発目標）に向けた環境への取り組みを行う。バイク事業に留まらず、デイトナが企業として社会に役立つ存在＝社会貢献であることを目指している。
