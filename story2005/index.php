                    2014年、デイトナはモーターサイクルガレージの取り扱いを開始する。デイトナに入社する前はスノーモービルを製造する会社にいた鈴木は、夏に製品を作ると、冬は人材派遣としてヤマハのバイク工場に出向した。任されたのはタンク塗装や車体組立、SRのヘッド加工など。塗装では今も乗り続けているTX650のタンクを手がけた。ヘッド加工ではバルブのあたり面を一番馬力が出るように公差の範囲内で「調整」。今につながる「チューニング」の原点と言える。さらに定時後は完成検査ではねられたバイクの「修理」。検査票ではねられた原因を調べ、必要なパーツを取りに行き、１台のバイクとして完成させる。若いから体力もあり、誰に強制されたわけでもなく、ただバイクに触っているのが楽しかった。鈴木にとって、まさに工場がバイクの学校だった。こうした経験から、鈴木は若い頃からバイクをいじるのが趣味で、テストコース脇には「紳さん小屋」と呼ばれる社員が自由に使えるガレージもあった。<br>
                    自身の家を新築する際、同時にガレージを建てると「これからの時代、ライダーの心を豊かにするには、バイクに乗らない時にも楽しめる空間が必要だ。ガレージがあれば好きな時にバイクを整備できるし、自慢の愛車をいつでも眺められる。デイトナがガレージの施工まで担当すれば、用品店スタッフも販売に専念できて助かるだろう」と考え、地元ガレージビルダーやメーカーに相談して「デイトナモーターサイクルガレージ」として事業化。今や自宅ガレージは市民権を得て、用品店で多くのライダーがガレージライフを気軽に楽しめるきっかけを作った。<br>
                    様々な側面からバイクの文化を創造する。この思いはデイトナの創業から今に至るまで変わることがない。
