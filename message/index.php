                      <h4>一度は諦めかけた道を、再び繋げてくれた存在</h4>
                      <p>デイトナ創立50周年、おめでとうございます。僕は17歳でレースを始めましたが、所属チームではレース以外の作業に追われて、自分の理想とするレース活動ができませんでした。「もっとレースに集中したい」とチームを飛び出しましたが、先のアテもなく「来年走れなければ（レースをやめて）地元に戻るしかないな」と思っていた時、阿部（当時、社長）さんに手紙を書いて、TEAM DAYTONAに所属することができました。それまでのレース参戦は全て自己資金でしたが、デイトナでは給料とメカニックがいて、僕は走ることに専念できました。初めてプロレーサーとして扱ってくれたのがデイトナでした。僕のレーサー人生、29年のうち20年をデイトナとともに走ってきて、デイトナには感謝の言葉しかありません。</p>
                    </li>
                    <li>
                      <h4>打倒ワークス、チーム一丸となって掴んだ勝利</h4>
                      <p>02年世界グランプリ鈴鹿での優勝も印象的ですが、一番記憶に残っているのは95年全日本ロード第5戦SUGOでの優勝です。当時チームデイトナはトッププライベーターでしたが、ワークスチームという高い壁がありました。レースに勝つにはワークスを倒すことが絶対条件で、市販レーサーのTZ250はあらゆる部分をチューンしました。それでもあと一歩で勝てなかった。そんなとき、当時世界GPで活躍し始めていたアプリリアRSV250を阿部さんが現地で即決購入し、用意してくれました。テストでTZとRSV250を乗り比べ、アプリリアの可能性に賭けました。というか、貴重なマシンを購入してもらって「乗る」しか選択肢はなかったですね（笑）。アプリリアは最初から速さはあったけど、安定性に欠けた部分もあり、メカニックと二人三脚でセッティング作業を繰り返してマシンを作りました。こうしてワークスを倒して勝ち取った全日本での優勝。これはレースを走った僕一人の力ではなく、阿部さんの執念と文字通りチーム一丸となって掴んだ勝利で、僕のレース人生でも一番の思い出です。表彰台の頂点に立ち、会場にいた阿部さんが泣いて喜んでくれたとき、ようやく一つ恩返しができたとホッとしたことを憶えています。</p>
                    </li>
                    <li>
                      <h4>ライダーに夢を与える商品を産み続けてほしい</h4>
                      <p>デイトナには、常に新しいことにチャレンジし続けてもらいたいと思っています。チームとして一緒にレースを戦ったデイトナには、ライダーをワクワクさせたり、あっと驚かせてくれるような夢のある商品が年に一つや二つあっても良いんじゃないかと思います。僕のレーサー時代もたくさんの失敗を重ねてきました。そんな時、阿部さんから「挑戦して失敗するのは当たり前、次に絶対失敗しないように、そこから学ぶことが大事」ということを教えてもらいました。これからのデイトナにも大いに期待しています。</p>
