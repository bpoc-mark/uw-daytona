                    <li>
                      <h4>箱付きでもスポーティ</h4>
                      <p>90年代に入って大型スポーツツアラーでロングツーリングを楽しむトレンドがあり、当時ハードケースは走りずらい、すり抜けしづらい、車体デザインを崩してカッコ悪いなどでソフトバッグを使っている人が多かったように思います。僕は95年にGPZ1100パニアケース仕様、99年は専用サイドケース装備の1000GTR／CONCOURSに乗っており、いわゆる「箱付き」は皆が心配するほど走行性能をスポイルしないと知っていました。その後GIVIのトップ＆サイドケースをつけたXJR1300でタンデム＆ロングツーリングする二輪専門誌の連載企画で、北は宮城県、南は熊本県、鳥取や新潟など全国を走って「箱付き」の正しい情報を伝えることができました。GIVIケースで思い出深いのは、デイトナのCBR1100XXスーパーブラックバード・フルパニア仕様。プレス向け試乗会で「箱付きでもスポーティな走りができることを証明したい!」と当時のGIVI担当者にOKをもらい、フルバンク＆膝擦り走行を披露しました。デイトナとは2003年頃からスクーターやミニバイク系の製品開発や性能検証でお付き合いがあり、ちょっとだけ恩返しの意味合いもありました。僕らが思っていた以上にインパクトのある走行シーンだったみたいで、今でも語り草になっているようです。</p>
                    </li>
                    <li>
                      <h4>バイクのある風景が当たり前の世界</h4>
                      <p>僕が子供の頃、商店の前にスーパーカブが停まっているのはごく自然な風景、街で見かけるバイクは颯爽としていて憧れを抱きました。仮面ライダーを筆頭に、ヒーローはバイクをカッコよく乗り回していた。群れてやかましい暴走行為は嫌いだけれど、一人ひとりと話してみれば根っこの部分は同じバイク好き。80年代バイクブームもリアルタイム&ど真ん中、大げさに言えば日本のバイク文化と共に歩んできたように思います。バイクは扱い方次第で、楽しめる乗り物にも危険な存在にもなりうる。マナーの悪さが新たな規制を作ってきた50年だったと思います。規制が増えれば息苦しく、とても楽しい気分では乗れない。これから先はマナー良くバイクと付き合うライダーが増えて、社会から認められる存在になれば良いなと思います。デイトナはヘンリーさんの渡米経験に由来する発見期、ニーズに合わせて新たなパーツやライディングギアを用意してきた創造期を経て、脱炭素を目指す社会の中でユーザーのために何ができるかを考える発想期に来ていると思います。これから先、デイトナに期待することは日本のバイク文化が世界をけん引するよう、ものづくりでその一役を担って欲しい。今よりもバイクが社会に受け入れられ、バイクのある風景が普遍的になるような世界観の手助けをして欲しいと思います。</p>
                    </li>
