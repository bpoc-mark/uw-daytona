                      <h4>スタビライザーでカスタムの楽しさを知る。</h4>
                      <p>創業50周年を迎えるデイトナ、おめでとうございます。今回改めてデイトナとの接点を話すと、高校2年（1984年）の頃、RZ250RRを新車で購入して、初めのうちはノーマルで満足していました。そのうち信号待ちで同じバイクと並んだ時にカスタム意欲が湧きました。とは言え、当時流行していたバックステップやレーシングチャンバーを買うのは予算的に厳しい。二輪専門誌の広告ページを見て、アルバイト収入で購入できそうだったのが、デイトナのフロントフォークスタビライザーでした。取り付けはボルトオンで簡単、フロントサスペンションの剛性が上がったか否かは体感できなかったけれど、量産型から「俺専用機」にアップグレードした満足感は十二分でした。</p>
